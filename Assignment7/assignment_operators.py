'''
a. Write a program to demonstrate the usage of at least three different assignment operators in Python (e.g., +=, -=, *=, /=)
by applying them to variables containing numerical values.
'''
# With '*=' operator
x=13
x*=3
print(x) # Output: 39

# With '-=' operator
x=13
x-=12
print(x) # Output: 1

# With '+=' operator
x=100
x/=10
print(x) # Output: 10
